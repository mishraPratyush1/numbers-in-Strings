package chapter5;

public class round_floor_ceil {

	public static void main(String[] args) 
	{
		System.out.println("rounded value "+Math.round(5.67)); //returns int value.
		System.out.println("ceiling value "+Math.ceil(9.4255f));
		System.out.println("flooring value "+Math.floor(9.4255f)); //returns double value.
	}

}
